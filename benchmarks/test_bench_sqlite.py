async def digest_txs(mempool, txs):
    for tx in txs:
        await mempool.digest_tx(tx)


async def del_tx_ids(mempool, tx_ids):
    await mempool.async_del_txids(tx_ids)


async def mempool_count(mempool):
    res = await mempool.async_fetchone("select count(*) from pos_messages")
    print(res[0])


